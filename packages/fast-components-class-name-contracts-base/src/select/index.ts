/**
 * The class name contract for the select component
 */
export interface SelectClassNameContract {
    /**
     * The root of the select component
     */
    select?: string;

    /**
     * The disabled modifier
     */
    select__disabled?: string;

    /**
     * The menu of the select component
     */
    select_menu?: string;

    /**
     * The menu open modifier
     */
    select_menu__open?: string;

    /**
     * The multi select modifier
     */
    select__multiSelectable?: string;

    /**
     * The root of the select's viewport positioner component
     */
    select_viewportPositioner?: string;

    /**
     * left positioner horizontal state
     */
    select_viewportPositioner__left?: string;

    /**
     * right positioner horizontal state
     */
    select_viewportPositioner__right?: string;

    /**
     * top positioner vertical state
     */
    select_viewportPositioner__top?: string;

    /**
     * bottom positioner vertical state
     */
    select_viewportPositioner__bottom?: string;

    /**
     * horizontal flip outward state
     */
    select_viewportPositioner__horizontalFlipOutward?: string;

    /**
     * horizontal flip inward state
     */
    select_viewportPositioner__horizontalFlipInward?: string;

    /**
     * vertical flip outward state
     */
    select_viewportPositioner__verticalFlipOutward?: string;

    /**
     * vertical flip inwarward state
     */
    select_viewportPositioner__verticalFlipInward?: string;
}
